# Even Numbers
# Print the even numbers from 1 to 99, inclusive. All numbers should be printed
# on separate lines.

# Input: None
# Output: Even numbers 1..99 on separate lines.
# Algorithm: starting at 2, loop while counter is less than 99, printing out
# each number, incrementing number += 2

num = 2
while num < 99
  puts num
  num += 2
end
